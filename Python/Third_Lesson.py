# Types of int and float

# int – Integer numbers
# int is used to represent whole numbers, without decimal points.
# It can represent both positive and negative values.
print (11)
print (-11)
print (0)

# float – Floating-point numbers
# float is used to represent numbers that contain decimal points.
# It can represent both positive and negative values with fractional parts.

print (11.1,9.5)
print (5.4,8.2)

# The type()function shaws the data type of a value in Python  
print ( type ('Joao') )
print ( type (32) )
print ( type (1.1) )

#Boolean - Boolean values
#Boolean values represent truth values : True or False
print(10 == 10) #True
print(10 ==100) #False
print(type(10 ==100)) #False